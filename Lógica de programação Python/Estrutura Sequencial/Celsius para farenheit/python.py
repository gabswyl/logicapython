# 10 - Faça um programa que peça a temperatura em graus Celsius transforme e mostre em graus fahrenheit

celsius = float(input('insira temperatura celsius para conversao em farenheit: '))

calcCelsius = (celsius * 1.8) + 32
resultadoCelsius = calcCelsius


print('Essa é a temperatura em Farenheit: ', resultadoCelsius)